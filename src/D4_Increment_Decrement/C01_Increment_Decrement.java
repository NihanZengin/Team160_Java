package D4_Increment_Decrement;

public class C01_Increment_Decrement {
    public static void main(String[] args) {

        int sayi= 20;

        // sayinin degerini 2 katina cikarin

        sayi *=2;
        System.out.println("11. satirda sayinin degeri :" + sayi); //11. satirda sayinin degeri :40

        //sayinin degrini 5 azaltin

        sayi -= 5;
        System.out.println("sayinin 16. satirda degeri: " + sayi); //sayinin 16. satirda degeri: 35

        //sayiyi 5'e bolup, sonucu sayi variable'na atayin

        sayi=sayi/5;

        System.out.println("22. satirda sayi degeri : " +sayi); //22. satirda sayi degeri : 7


    }
}
